// OPERADORES

// Aritmetico
console.log("Operadores Aritmeticos")
let num1 = 23;
let num2 = 14;

console.log("suma: ", (num1 + num2));
console.log("Resta: ", (num1 - num2));
console.log("Multiplicacion: ", (num1 * num2));
console.log("Division: ", (num1 / num2));

// Operadores de comparacion
console.log("Operadores Aritmeticos de comparacion");

const prioridad1 = 4;
const prioridad2 = 6;

console.log(prioridad1 > prioridad2);
console.log(prioridad1 === prioridad2);
console.log(prioridad1 < prioridad2);


console.log("Operadores Aritmeticos de Logicos");
const prioridadAlta = true;
const completada = false;

console.log(prioridadAlta && !completada);
console.log(prioridadAlta || !completada);
